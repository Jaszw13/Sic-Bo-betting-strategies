def check_win(bet_type, dice_results):
    """Checks if a given bet wins based on dice results, excluding triples for Big/Small."""
    d1, d2, d3 = dice_results
    total_sum = d1 + d2 + d3
    is_triple = (d1 == d2 == d3)

    if bet_type == 'Small':
        # Small bet wins if total is 4-10, and not a triple
        return (4 <= total_sum <= 10) and not is_triple
    elif bet_type == 'Big':
        # Big bet wins if total is 11-17, and not a triple
        return (11 <= total_sum <= 17) and not is_triple
    else:
        # Placeholder for other bet types if needed later
        raise ValueError(f"Unsupported bet type: {bet_type}")

def fixed_bet_strategy(initial_capital, target_capital, min_bet, max_bet, max_rounds, bet_type, sic_bo_bets_df_local):
    """Simulates a single run of the fixed-bet strategy.

    Args:
        initial_capital (int): Starting capital.
        target_capital (int): Capital to reach to win.
        min_bet (int): Minimum bet amount (fixed bet).
        max_bet (int): Maximum allowable bet (for clipping current_bet).
        max_rounds (int): Maximum number of rounds to play.
        bet_type (str): Type of bet ('Big' or 'Small').
        sic_bo_bets_df_local (pd.DataFrame): DataFrame containing bet details.

    Returns:
        tuple: (final_capital, rounds_played, capital_history)
    """
    current_capital = initial_capital
    current_bet = min_bet
    rounds_played = 0
    capital_history = [initial_capital]

    payout_ratio = sic_bo_bets_df_local.loc[sic_bo_bets_df_local['Bet Type'] == bet_type, 'Payout Ratio'].iloc[0]

    while 0 < current_capital < target_capital and rounds_played < max_rounds:
        rounds_played += 1

        # Ensure bet does not exceed current capital
        actual_bet = min(current_bet, current_capital)
        if actual_bet == 0:
            break # Cannot place a bet, essentially bankrupt

        current_capital -= actual_bet
        dice_results = roll_dice()

        if check_win(bet_type, dice_results):
            current_capital += actual_bet * (1 + payout_ratio)

        capital_history.append(current_capital)

    return current_capital, rounds_played, capital_history

def martingale_strategy(initial_capital, target_capital, min_bet, max_bet, max_rounds, bet_type, sic_bo_bets_df_local):
    """Simulates a single run of the Martingale strategy.

    Args:
        initial_capital (int): Starting capital.
        target_capital (int): Capital to reach to win.
        min_bet (int): Minimum bet amount (starting bet).
        max_bet (int): Maximum allowable bet.
        max_rounds (int): Maximum number of rounds to play.
        bet_type (str): Type of bet ('Big' or 'Small').
        sic_bo_bets_df_local (pd.DataFrame): DataFrame containing bet details.

    Returns:
        tuple: (final_capital, rounds_played, capital_history)
    """
    current_capital = initial_capital
    current_bet = min_bet
    rounds_played = 0
    capital_history = [initial_capital]

    payout_ratio = sic_bo_bets_df_local.loc[sic_bo_bets_df_local['Bet Type'] == bet_type, 'Payout Ratio'].iloc[0]

    while 0 < current_capital < target_capital and rounds_played < max_rounds:
        rounds_played += 1

        # Adjust current_bet for max_bet and available capital
        bet_to_place = min(current_bet, max_bet, current_capital)

        if bet_to_place <= 0: # Cannot place a bet, essentially bankrupt
            break

        current_capital -= bet_to_place
        dice_results = roll_dice()

        if check_win(bet_type, dice_results):
            current_capital += bet_to_place * (1 + payout_ratio)
            current_bet = min_bet # Reset bet on win
        else:
            current_bet *= 2 # Double bet on loss

        capital_history.append(current_capital)

    return current_capital, rounds_played, capital_history

